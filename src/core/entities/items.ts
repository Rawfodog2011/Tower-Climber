export function canClassEquipItem(playerClassId: string, item: Item): boolean {
  // Se não houver restrição, permite
  if (!item.allowedClassIds || item.allowedClassIds.length === 0) {
    return true;
  }

  let currentClass: ClassDefinition | undefined = CLASSES[playerClassId];

  // Percorre a árvore de classes de baixo para cima
  while (currentClass) {
    if (item.allowedClassIds.includes(currentClass.id)) {
      return true; // Encontrou permissão nesta classe ou em algum ancestral
    }
    
    // Sobe para o parent
    if (currentClass.parentClassId) {
      currentClass = CLASSES[currentClass.parentClassId];
    } else {
      break;
    }
  }

  return false;
}

export function getRandomItemByRarityAndClass(rarity: Rarity, classId: string): Item | null {
  const pool = Object.values(ITEMS_DATABASE).filter(item => 
    item.rarity === rarity && canClassEquipItem(classId, item)
  );

  if (pool.length === 0) return null;
  const item = pool[Math.floor(random() * pool.length)];
  return applyManufacturer(item);
}

/**
 * Retorna um item aleatório baseado em uma determinada raridade.
 */
export function getRandomItemByRarity(rarity: Rarity): Item | null {
  const items = Object.values(ITEMS_DATABASE).filter(i => i.rarity === rarity);
  if (items.length === 0) return null;
  const index = Math.floor(random() * items.length);
  return items[index];
}

export function getItemTier(item: Item): number {
  if (!item.allowedClassIds || item.allowedClassIds.length === 0) return 1;
  let maxLevel = 1;
  for (const classId of item.allowedClassIds) {
    const classDef = CLASSES[classId];
    if (classDef && classDef.requiredLevel > maxLevel) {
      maxLevel = classDef.requiredLevel;
    }
  }
  return maxLevel;
}

export function getRandomItemForFloor(rarity: Rarity, floor: number): Item | null {
  const itemsOfRarity = Object.values(ITEMS_DATABASE).filter(i => i.rarity === rarity);
  if (itemsOfRarity.length === 0) return null;

  const weightedItems = itemsOfRarity.map(item => {
    const tier = getItemTier(item);
    const diff = floor - tier;
    let weight = 10;
    
    if (diff < 0) {
       weight = Math.max(1, Math.floor(10 / Math.abs(diff)));
    } else if (diff < 20) {
       weight = 20 + diff * 4;
    } else if (diff < 40) {
       weight = 100;
    } else {
       weight = Math.max(5, 100 - (diff - 40) * 2);
    }
    
    return { item, weight };
  });

  const validItems = weightedItems.filter(i => i.weight > 0);
  if (validItems.length === 0) {
    const index = Math.floor(random() * itemsOfRarity.length);
    return applyManufacturer(itemsOfRarity[index]);
  }

  const totalWeight = validItems.reduce((acc, curr) => acc + curr.weight, 0);
  let roll = random() * totalWeight;

  for (const { item, weight } of validItems) {
    roll -= weight;
    if (roll <= 0) return applyManufacturer(item);
  }

  return applyManufacturer(validItems[validItems.length - 1].item);
}
