function applyManufacturer(item: Item): Item {
  let processed = item;
  if (processed.rarity !== 'common' && random() <= 0.5) {
    const mfs: Manufacturer[] = ['Kinetix', 'AeroDynamics', 'OmniCorp'];
    const manufacturer = mfs[Math.floor(random() * mfs.length)];
    processed = {
      ...processed,
      id: `${processed.id}_${manufacturer}`,
      name: `${manufacturer} ${processed.name}`,
      manufacturer
    };
  }
  return applyHardwareSlots(processed);
}
